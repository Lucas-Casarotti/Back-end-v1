using System;

namespace Aula17
{
    class Program
    {
        static void Main(string[] args)
        {
            // ESTRUTURA DE REPETIÇÃO WHILE
            // CONTANDO DE 0 ATÉ 5
            int valor = 0;
            while(valor <= 5)
            {
                Console.WriteLine(valor);
                valor++;
            }
        }
    }
